; Atari XL PBI VERA register map.
; The board is selected by writing $80 to $D1FF and then exposes the
; VERA register window at $D100-$D11F.

PBI_ACTIVATION    = $D1FF
VERA_BASE         = $D100

VERA_ADDR_LOW     = VERA_BASE + $00
VERA_ADDR_HIGH    = VERA_BASE + $01
VERA_ADDR_BANK    = VERA_BASE + $02
VERA_DATA0        = VERA_BASE + $03
VERA_DATA1        = VERA_BASE + $04
VERA_CTRL         = VERA_BASE + $05

VERA_IEN          = VERA_BASE + $06
VERA_ISR          = VERA_BASE + $07
VERA_IRQLINE_L    = VERA_BASE + $08

VERA_DC_VIDEO     = VERA_BASE + $09  ; DCSEL=0
VERA_DC_HSCALE    = VERA_BASE + $0A  ; DCSEL=0
VERA_DC_VSCALE    = VERA_BASE + $0B  ; DCSEL=0
VERA_DC_BORDER    = VERA_BASE + $0C  ; DCSEL=0

VERA_DC_HSTART    = VERA_BASE + $09  ; DCSEL=1
VERA_DC_HSTOP     = VERA_BASE + $0A  ; DCSEL=1
VERA_DC_VSTART    = VERA_BASE + $0B  ; DCSEL=1
VERA_DC_VSTOP     = VERA_BASE + $0C  ; DCSEL=1

VERA_FX_CTRL      = VERA_BASE + $09  ; DCSEL=2
VERA_FX_TILEBASE  = VERA_BASE + $0A  ; DCSEL=2
VERA_FX_MAPBASE   = VERA_BASE + $0B  ; DCSEL=2
VERA_FX_MULT      = VERA_BASE + $0C  ; DCSEL=2

VERA_FX_X_INCR_L  = VERA_BASE + $09  ; DCSEL=3
VERA_FX_X_INCR_H  = VERA_BASE + $0A  ; DCSEL=3
VERA_FX_Y_INCR_L  = VERA_BASE + $0B  ; DCSEL=3
VERA_FX_Y_INCR_H  = VERA_BASE + $0C  ; DCSEL=3

VERA_FX_X_POS_L   = VERA_BASE + $09  ; DCSEL=4
VERA_FX_X_POS_H   = VERA_BASE + $0A  ; DCSEL=4
VERA_FX_Y_POS_L   = VERA_BASE + $0B  ; DCSEL=4
VERA_FX_Y_POS_H   = VERA_BASE + $0C  ; DCSEL=4

VERA_FX_X_POS_S   = VERA_BASE + $09  ; DCSEL=5
VERA_FX_Y_POS_S   = VERA_BASE + $0A  ; DCSEL=5
VERA_FX_POLY_FILL_L = VERA_BASE + $0B ; DCSEL=5
VERA_FX_POLY_FILL_H = VERA_BASE + $0C ; DCSEL=5

VERA_FX_CACHE_L   = VERA_BASE + $09  ; DCSEL=6
VERA_FX_ACCUM_RESET = VERA_BASE + $09 ; DCSEL=6
VERA_FX_CACHE_M   = VERA_BASE + $0A  ; DCSEL=6
VERA_FX_ACCUM     = VERA_BASE + $0A  ; DCSEL=6
VERA_FX_CACHE_H   = VERA_BASE + $0B  ; DCSEL=6
VERA_FX_CACHE_U   = VERA_BASE + $0C  ; DCSEL=6

VERA_DC_VER0      = VERA_BASE + $09  ; DCSEL=63
VERA_DC_VER1      = VERA_BASE + $0A  ; DCSEL=63
VERA_DC_VER2      = VERA_BASE + $0B  ; DCSEL=63
VERA_DC_VER3      = VERA_BASE + $0C  ; DCSEL=63

VERA_L0_CONFIG    = VERA_BASE + $0D
VERA_L0_MAPBASE   = VERA_BASE + $0E
VERA_L0_TILEBASE  = VERA_BASE + $0F
VERA_L0_HSCROLL_L = VERA_BASE + $10
VERA_L0_HSCROLL_H = VERA_BASE + $11
VERA_L0_VSCROLL_L = VERA_BASE + $12
VERA_L0_VSCROLL_H = VERA_BASE + $13

VERA_L1_CONFIG    = VERA_BASE + $14
VERA_L1_MAPBASE   = VERA_BASE + $15
VERA_L1_TILEBASE  = VERA_BASE + $16
VERA_L1_HSCROLL_L = VERA_BASE + $17
VERA_L1_HSCROLL_H = VERA_BASE + $18
VERA_L1_VSCROLL_L = VERA_BASE + $19
VERA_L1_VSCROLL_H = VERA_BASE + $1A

VERA_AUDIO_CTRL   = VERA_BASE + $1B
VERA_AUDIO_RATE   = VERA_BASE + $1C
VERA_AUDIO_DATA   = VERA_BASE + $1D

VERA_SPI_DATA     = VERA_BASE + $1E
VERA_SPI_CTRL     = VERA_BASE + $1F

VERA_PALETTE      = $1FA00
VERA_SPRITES      = $1FC00

; X16-only debug helpers referenced by utils.s.
; The Atari port does not use them, but the symbols must exist.
VIA2_PORTB        = 0
VIA2_DDRB         = 0

ACTIVATE_VERA .macro
    lda #$80
    sta PBI_ACTIVATION
.endmacro
